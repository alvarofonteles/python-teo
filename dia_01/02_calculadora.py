# %%
# Soma
print('1 + 1 =', 1 + 1)

# Subtração
print('10 - 5 =', 10 - 5)

# %%
# Multiplicação
print('15 * 3 =', 15 * 3)

# %%
# Divisão
print('10 / 3 =', 10/3)

# primeira parte //
print('10 / 3 =', 10 // 3)

# Divisão Fração
print('10 / 4 =', 10/4)

# Resto da Divisão (Mod %)
# 10 / 4 = 2,5 , então 10 - 4 = 6
# e 6 - 4 = 2
print('10 % 4 = ', 10 % 4)

# Potência
print('3 ** 2 =', 3**2)
print('3 ** 3 =', 3**3)
print('3 ** 4 =', 3**4)
